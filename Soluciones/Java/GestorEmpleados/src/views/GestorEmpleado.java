    public void verDatos(){
        DecimalFormat df = new DecimalFormat("####.00");
        String cod, nom, sue;
        Nodo aux = inicio;
        num = 0;
        TextAreaDatos.setText("");
        Encabezado();
        while(aux != null){
            cod = aux.codigo;
            nom = aux.nombre;
            sue = aux.sueldo;
            num++;
            String numero = String.valueOf(num);
            sue = df.format(Double.valueOf(sue));
            System.out.println("\t"+numero+"\t"+cod+"\t"+nom+"\t"+sue);
            TextAreaDatos.append("\t"+numero+"\t"+cod+"\t"+nom+"\t"+sue+"\n");
            aux = aux.sig;
        }
    }
    
    public Nodo buscar(String cod){
        Nodo pos = inicio;
        while(pos != null  && !cod.equalsIgnoreCase(pos.codigo)){
            pos = pos.sig;
        }
        return pos;
    }
    
    Nodo Buscar(Nodo inicio, String cod){
        Nodo pos = inicio;
        while(pos!=null && !cod.equalsIgnoreCase(pos.codigo)){
            pos=pos.sig;
        }
        return pos;
    }
    
    void eliminar(Nodo actual){
       Nodo anterior = inicio;
       while(anterior.sig != actual && anterior.sig != null){
           anterior = anterior.sig;
       }
       if(actual != null){
           if(anterior == actual){
               inicio = actual.sig;
           }else{
               anterior.sig = actual.sig;
           }
       }
    }
    
    Nodo InsertaInicio(Nodo inicio ,String cod, String nom,String suel){
        Nodo nuevo = new Nodo(cod, nom, suel);
        nuevo.sig=inicio;
        inicio=nuevo;
        return inicio;
    }

